public class ClientFactory {
    private static final ClientFactory ourInstance = new ClientFactory();
    private static final String URLMobileApp = "https://agentm.azurewebsites.net";
    /**
     * Mobile Service Client reference
     */
    private MobileServiceClient mClient;

    public static ClientFactory getInstance() {
        return ourInstance;
    }

    private ClientFactory() {
    }

    public void initClient(Context context) throws MalformedURLException {
        // Mobile Service URL and key
        mClient = new MobileServiceClient(URLMobileApp, context);

        // Extend timeout from default of 10s to 20s
        mClient.setAndroidHttpClientFactory(new OkHttpClientFactory() {
            @Override
            public OkHttpClient createOkHttpClient() {
                OkHttpClient client = new OkHttpClient();
                client.setReadTimeout(20, TimeUnit.SECONDS);
                client.setWriteTimeout(20, TimeUnit.SECONDS);
                return client;
            }
        });
    }

    public MobileServiceClient getClient() {
        return mClient;
    }

}
